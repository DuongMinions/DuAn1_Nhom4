    private void btnMuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMuaActionPerformed
        int row = tblSanPham.getSelectedRow();

        vSanPham v = SP1.getAllVSP().get(row);
        vPhieuHoaDon hd1 = new vPhieuHoaDon();
        hd1.setIDSP(v.getID());
        hd1.setTENSP(v.getTenSanPham());
        hd1.setGIABAN(v.getGiaBan());
        hd1.setSIZE(v.getSize());
        hd1.setMAUSAC(v.getMauSac());
        hd1.setHINHANH(v.getHinhAnh());
        hd1.setNGAYDATHANG(new Date());
        int SoLuong = Integer.valueOf(JOptionPane.showInputDialog("Nhập số lượng:"));
        hd1.setSoLuong(SoLuong);
        int soLuong = v.getSoLuong() - hd1.getSoLuong();
        v.setSoLuong(soLuong);
        String DonGia = String.valueOf(hd1.getGIABAN());
        int ThanhTien = (int) (SoLuong * Double.valueOf(DonGia));
         BigDecimal tempBig = new BigDecimal(Double.toString(ThanhTien));
        hd1.setTHANHTIEN(tempBig);
        int chon = JOptionPane.showConfirmDialog(this, "Bạn có muốn đặt hàng không?");
        if (chon != JOptionPane.YES_OPTION) {
            return;
        }
        hd.them1(hd1);
      
        new PhieuHoaDon().setVisible(true);

    }//GEN-LAST:event_btnMuaActionPerformed


