    private void fillToTable(ArrayList<vSanPham> listVsp) {
        mol = (DefaultTableModel) tblSanPham.getModel();
        mol.setRowCount(0);
        for (int i = 0; i < listVsp.size(); i++) {
            Object[] data = new Object[]{i + 1, listVsp.get(i).getTenSanPham(), listVsp.get(i).getSoLuong(),
                listVsp.get(i).getSize(), listVsp.get(i).getMauSac(), listVsp.get(i).getChatLieu(), listVsp.get(i).getGiaNhap(), listVsp.get(i).getTenLoai(), listVsp.get(i).getHinhAnh(), listVsp.get(i).TrangThai(), listVsp.get(i).getID()};
            mol.addRow(data);
        }

    }

