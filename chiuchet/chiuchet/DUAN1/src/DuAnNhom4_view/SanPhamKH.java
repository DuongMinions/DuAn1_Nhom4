    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        int row = tblSanPham.getSelectedRow();

        vSanPham v = SP1.getAllVSP().get(row);
        GioHang gh = new GioHang();
        gh.setIDSanPham(v.getID());
        gh.setTensp(v.getTenSanPham());
        gh.setDonGia(v.getGiaBan());
        gh.setHinhAnh(v.getHinhAnh());
        gh.setNgayThemVaoGioHang(new Date());
        int SoLuong = Integer.valueOf(JOptionPane.showInputDialog("Nhập số lượng:"));
        gh.setSoLuong(SoLuong);
        int soLuong = v.getSoLuong() - gh.getSoLuong();
        v.setSoLuong(soLuong);
        for (GioHang x : listGioHang) {
            if (v.getID().equals(v.getID())) {
                int _soLuong = x.getSoLuong() + gh.getSoLuong();
                x.setSoLuong(_soLuong);
                return;
            }
        }
        int chon = JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm vào giỏ hàng không?");
        if (chon != JOptionPane.YES_OPTION) {
            return;
        }
        gh1.themSanPhamVaoGioHang(gh);
        JOptionPane.showMessageDialog(this, "Thêm vào giỏ hàng thành công");
    }//GEN-LAST:event_btnThemActionPerformed

